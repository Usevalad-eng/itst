public class TMP {
    public void myMetod() {
        int r = 11;
        System.out.println("я получил на вход число" + " " + r);

    }

    public void myMetod2() {
        long r = 111;
        System.out.println("я получил на вход длинное число" + " " + r);

    }

    public void myMetod3() {
        char r = 'a';
        System.out.println("я получил на вход символ" + " " + r);

    }
    public void myMetod4() {
        float r = (float) 1.4;
        System.out.println("я получил на вход дробное" + " " + r);
    }
    public void myMetod5() {
        double r = (double) 1.4;
        System.out.println("я получил на вход длинное дробное" + " " + r);
    }
    public void myMetod6() {
        short r = (short) 1.4;
        System.out.println("я получил на вход  короткое" + " " + r);
    }
    public void myMetod7() {
        byte r = (byte) 1;
        System.out.println("я получил на вход  очень короткое" + " " + r);
    }
    public void myMetod8() {
        boolean r = (boolean) true;
        System.out.println("я получил на вход  булевое" + " " + r);
    }
}
